  explanationDiv.style.marginTop = '0.5rem';
  explanationDiv.style.color = '#6c757d';
  explanationDiv.textContent = `üí° ${questionObj.explanation}`;
  feedback.appendChild(explanationDiv);

  document.getElementById('score').textContent = `‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô: ${score}`;

  const answerButtons = document.querySelectorAll('#answers button');
  answerButtons.forEach(btn => btn.disabled = true);

  document.getElementById('nextBtn').classList.remove('hidden');
